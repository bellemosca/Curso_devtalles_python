value = 100
type_value = type(value)

print(value)
print(type_value)

# Int para String
value = str(100) + "Oi"
type_value = type(value)

print(value)
print(type_value)

# String para Int
value = int("100")
type_value = type(value)

print(value)
print(type_value)

# String para float
value = float("100")
type_value = type(value)

print(value)
print(type_value)
