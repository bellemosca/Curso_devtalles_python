# Igualdade
print(5 == 5)  # True
print(5 == 3)  # False
print("oi" == "oi")  # True

# Desigualdade
print(5 != 5)  # False
print(5 != 3)  # True
print("oi" != "hello")  # True

# Maior que
print(7 > 5)  # True
print(5 > 9)  # False

# Menor que
print(7 < 5)  # False
print(5 < 9)  # True

# Maior ou igual a
print(7 >= 5)  # True
print(5 >= 9)  # False
print(5 >= 5)  # True

# Menor ou igual a
print(7 <= 5)  # False
print(5 <= 9)  # True
print(5 <= 5)  # True
